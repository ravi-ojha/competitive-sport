#include <bits/stdc++.h>
using namespace std;
int main()
{
	cout << setw(10) << "type" << setw(7) << "bytes" << endl;
	cout << setw(10) << "-------" << setw(7) << "------" << endl;
	cout << setw(10) << "bool" << setw(7) << sizeof(bool) << endl;
	cout << setw(10) << "char" << setw(7) << sizeof(char) << endl;
	cout << setw(10) << "short" << setw(7) << sizeof(short) << endl;
	cout << setw(10) << "int" << setw(7) << sizeof(int) << endl;
	cout << setw(10) << "long" << setw(7) << sizeof(long) << endl;
	cout << setw(10) << "long long" << setw(7) << sizeof(long long) << endl;
	cout << setw(10) << "float" << setw(7) << sizeof(float) << endl;
	cout << setw(10) << "double" << setw(7) << sizeof(double) << endl;
	return 0;
}
